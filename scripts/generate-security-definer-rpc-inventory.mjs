#!/usr/bin/env node
/**
 * Regenerate docs/ops/security-definer-rpc-inventory.md from the local Supabase DB.
 * Requires a running local stack (docker container supabase_db_*).
 *
 * Usage: node scripts/generate-security-definer-rpc-inventory.mjs
 */

import { execFileSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import allowlists from './security-definer-rpc-allowlists.json' with { type: 'json' };

const repoRoot = path.resolve(import.meta.dirname, '..');
const outPath = path.join(repoRoot, 'docs/ops/security-definer-rpc-inventory.md');

function resolveDbContainer() {
  const names = execFileSync('docker', ['ps', '--format', '{{.Names}}'], {
    encoding: 'utf8',
  })
    .split(/\r?\n/)
    .map((n) => n.trim())
    .filter(Boolean);
  const match = names.find((n) => n.startsWith('supabase_db_'));
  if (!match) {
    throw new Error('No supabase_db_* container found. Start the local stack first.');
  }
  return match;
}

function categorize(name, hasTrigger) {
  if (hasTrigger === 't') return 'internal_trigger';
  if (allowlists.anonPublicRpc.includes(name)) return 'anon_public_rpc';
  if (allowlists.rlsPredicateHelpers?.includes(name)) return 'rls_predicate_helper';
  if (allowlists.authenticatedPublicRpc.includes(name)) return 'authenticated_public_rpc';
  return 'internal_or_service';
}

function queryInventory(container) {
  const sql = `
SELECT
  p.proname,
  pg_get_function_identity_arguments(p.oid) AS args,
  EXISTS (
    SELECT 1 FROM pg_trigger t
    WHERE t.tgfoid = p.oid AND NOT t.tgisinternal
  ) AS has_user_trigger,
  has_function_privilege('anon', p.oid, 'EXECUTE') AS anon_exec,
  has_function_privilege('authenticated', p.oid, 'EXECUTE') AS auth_exec
FROM pg_proc p
JOIN pg_namespace n ON n.oid = p.pronamespace
WHERE n.nspname = 'public'
  AND p.prokind = 'f'
  AND p.prosecdef
ORDER BY p.proname, args;
`;

  const raw = execFileSync(
    'docker',
    ['exec', container, 'psql', '-U', 'postgres', '-d', 'postgres', '-At', '-F', '\t', '-c', sql],
    { encoding: 'utf8' },
  );

  return raw
    .trim()
    .split(/\r?\n/)
    .filter(Boolean)
    .map((line) => {
      const [name, args, hasTrigger, anonExec, authExec] = line.split('\t');
      return {
        name,
        args,
        category: categorize(name, hasTrigger),
        anonExec,
        authExec,
      };
    });
}

function buildMarkdown(rows) {
  const generatedAt = new Date().toISOString();
  const anonCallable = rows.filter((r) => r.anonExec === 't');

  let md = `# SECURITY DEFINER RPC grant inventory\n\n`;
  md += `> Auto-generated by \`node scripts/generate-security-definer-rpc-inventory.mjs\`. `;
  md += `Do not hand-edit the table; regenerate after grant changes.\n\n`;
  md += `- Generated (UTC): ${generatedAt}\n`;
  md += `- Total \`public\` SECURITY DEFINER functions: ${rows.length}\n`;
  md += `- Callable by \`anon\` after lockdown: ${anonCallable.length}\n\n`;
  md += `Policy reference: [security-definer-rpc-policy.md](./security-definer-rpc-policy.md)\n\n`;
  md += `| Function | Arguments | Category | anon EXECUTE | authenticated EXECUTE |\n`;
  md += `| --- | --- | --- | --- | --- |\n`;

  for (const row of rows) {
    md += `| \`${row.name}\` | \`${row.args || '(none)'}\` | ${row.category} | ${row.anonExec === 't' ? 'yes' : 'no'} | ${row.authExec === 't' ? 'yes' : 'no'} |\n`;
  }

  return md;
}

function main() {
  const container = resolveDbContainer();
  const rows = queryInventory(container);
  const markdown = buildMarkdown(rows);
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, markdown, 'utf8');
  console.log(`Wrote ${rows.length} rows to ${path.relative(repoRoot, outPath)}`);
}

main();
